package br.com.betohayasida.webcrawler.Store;

/**
 * Class for storing links
 */
public class Link {
	private String url;
	private String text;
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	
}
